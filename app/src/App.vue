          answerOptions: [],
        }
      },

      prevQuestion() {
        if (this.currentQuestionIdx > 0) {
          this.currentQuestionIdx -= 1
        }
      },
  
      nextQuestion() {
        this.currentQuestionIdx += 1
        if (this.currentQuestionIdx > this.questions.length - 1) {
